import mongoose from "mongoose";

const userSchema = mongoose.Schema(
  {
    fristname: {
      type: String,
      required: true,
    },
    lastname: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export const users = mongoose.model("Users", userSchema);
